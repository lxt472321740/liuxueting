<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				fuwubianhao: this.getUUID(),
				fuwuxiangmu: '',
				fuwuleixing: '',
				xiangmudengji: '',
				fuwujiage: '',
				fuwufengmian: '',
				faburiqi: '',
				fuwujieshao: '',
				},
				fuwuleixingOptions: [],
				fuwuleixingIndex: 0,
				xiangmudengjiOptions: [],
				xiangmudengjiIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   fuwubianhao : false,
                   fuwuxiangmu : false,
                   fuwuleixing : false,
                   xiangmudengji : false,
                   fuwujiage : false,
                   fuwufengmian : false,
                   faburiqi : false,
                   fuwujieshao : false,
                   clicktime : false,
                   clicknum : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.faburiqi = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下拉框
			res = await this.$api.option(`fuwuleixing`,`fuwuleixing`,{});
			this.fuwuleixingOptions = res.data;
            this.fuwuleixingOptions.unshift("请选择服务类型");
			// 自定义下拉框值
			this.xiangmudengjiOptions = "中等,中高等,高等".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fuwuxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='fuwubianhao'){
					this.ruleForm.fuwubianhao = obj[o];
					this.ro.fuwubianhao = true;
					continue;
					}
					if(o=='fuwuxiangmu'){
					this.ruleForm.fuwuxiangmu = obj[o];
					this.ro.fuwuxiangmu = true;
					continue;
					}
					if(o=='fuwuleixing'){
					this.ruleForm.fuwuleixing = obj[o];
					this.ro.fuwuleixing = true;
					continue;
					}
					if(o=='xiangmudengji'){
					this.ruleForm.xiangmudengji = obj[o];
					this.ro.xiangmudengji = true;
					continue;
					}
					if(o=='fuwujiage'){
					this.ruleForm.fuwujiage = obj[o];
					this.ro.fuwujiage = true;
					continue;
					}
					if(o=='fuwufengmian'){
					this.ruleForm.fuwufengmian = obj[o].split(",")[0];
					this.ro.fuwufengmian = true;
					continue;
					}
					if(o=='faburiqi'){
					this.ruleForm.faburiqi = obj[o];
					this.ro.faburiqi = true;
					continue;
					}
					if(o=='fuwujieshao'){
					this.ruleForm.fuwujieshao = obj[o];
					this.ro.fuwujieshao = true;
					continue;
					}
					if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					this.ro.clicktime = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
		},
		methods: {
            fuwujieshaoChange(e) {
                this.ruleForm.fuwujieshao = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			faburiqiChange(e) {
				this.ruleForm.faburiqi = e.target.value;
				this.$forceUpdate();
			},

			// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},

			// 下拉变化
			fuwuleixingChange(e) {
				this.fuwuleixingIndex = e.target.value
				this.ruleForm.fuwuleixing = this.fuwuleixingOptions[this.fuwuleixingIndex]
			},
			// 下拉变化
			xiangmudengjiChange(e) {
				this.xiangmudengjiIndex = e.target.value
				this.ruleForm.xiangmudengji = this.xiangmudengjiOptions[this.xiangmudengjiIndex]
			},

			fuwufengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fuwufengmian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {





















//跨表计算判断
				var obj;
				if(this.ruleForm.fuwujiage&&(!this.$validate.isNumber(this.ruleForm.fuwujiage))){
					this.$utils.msg(`服务价格应输入数字`);
					return
				}
				if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`fuwuxinxi`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`fuwuxinxi`, this.ruleForm);
						}else{
							await this.$api.add(`fuwuxinxi`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`fuwuxinxi`, this.ruleForm);
					}else{
						await this.$api.add(`fuwuxinxi`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
